// Add a helper function to filter schools by category
export const getSchoolsByCategory = (category) => {
  const categoryRanges = {
    nursery: { min: 7, max: 8 },
    primary: { min: 9, max: 10 },
    secondary: { min: 11, max: 12 },
    tertiary: { min: 1, max: 6 }
  }

  const range = categoryRanges[category]
  return schools.filter((school) => school.id >= range.min && school.id <= range.max)
}

